#include <stdio.h>
int main()
{
    float A;
    float B;
    printf("Enter the value of A: ");
    scanf("%f",&A);
    printf("Enter the value of B: ");
    scanf("%f",&B);
    printf("SUM: %f \n",A+B);
    printf("SUBSTRACTION: %f \n",A-B);
    printf("MULTIPLICATION: %f \n",A*B);
    printf("DIVISION: %f \n",A/B);
    return 0;
}